{
    id = setid;
}

pair<int, vector<int> *> edge::get_point(int order)
{
    return point[order];
    //return *((point[order]).second);
}

void edge::set_point(pair<int, vector<int> *> setp)
{
    point.push_back(setp);
}

int edge::get_size()
{
    return point.size();
}